document.getElementById("playerCur").onclick = function() {
    if (document.getElementById("playerSelect").style.display == "flex") {
        document.getElementById("playerSelect").style.display = "none";
    } else {
        if (formulateSquadrine()) {
            document.getElementById("custom").hidden = false;
        } else {
            document.getElementById("custom").hidden = true;
        }
        document.getElementById("playerSelect").style.display = "flex";
    }
}

